package com.sinohealth.eszservice.dto.ad;

import java.io.Serializable;

public class ContentDto implements Serializable{
	private static final long serialVersionUID = 1L;
	
	private Integer categoryId;   	   /*广告栏目Id*/
	private String colId;   	       /*资讯栏目Id*/
	private String type;     		   /*广告类型*/
	private String title;     		   /*标题*/
	private String pic;       		   /*广告图片*/
	private String position;           /*广告展示位置*/
	private String link;       		   /*链接地址*/
	private Integer num = 1;       		   /*返回记录数*/
	private String appName;	    /*app的标识符*/
	
	public Integer getCategoryId() {
		return categoryId;
	}
	public void setCategoryId(Integer categoryId) {
		this.categoryId = categoryId;
	}	
	public String getColId() {
		return colId;
	}
	public void setColId(String colId) {
		this.colId = colId;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getPic() {
		return pic;
	}
	public void setPic(String pic) {
		this.pic = pic;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public Integer getNum() {
		return num;
	}
	public void setNum(Integer num) {
		this.num = num;
	}
	public String getAppName() {
		return appName;
	}
	public void setAppName(String appName) {
		this.appName = appName;
	}	
	
}
