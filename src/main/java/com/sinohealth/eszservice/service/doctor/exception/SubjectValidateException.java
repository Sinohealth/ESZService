package com.sinohealth.eszservice.service.doctor.exception;

public class SubjectValidateException extends Exception {

	private static final long serialVersionUID = -5665948570108722668L;

	private int errCode;

	private String message;

	public SubjectValidateException() {
		super();
	}
	
	public SubjectValidateException(String message,int errCode) {
		super();
		this.errCode = errCode;
		this.message = message;
	}

	public int getErrCode() {
		return errCode;
	}

	public void setErrCode(int errCode) {
		this.errCode = errCode;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
	
}
